public class Reminder {
    public long _id;
    private String title;
    private String description;
    private long time;

    public Reminder() {
    }

    public Reminder(long _id, String title, String description, long time) {
        this.title = title;
        this._id = _id;
        this.description = description;
        this.time = time;
    }

    public long get_id() {
        return _id;
    }

    public void set_id(long _id) {
        this._id = _id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public long getTime() {
        return time;
    }

    public void setTime(long time) {
        this.time = time;
    }
}
