public class EnrollPost extends SchedulePost {
    private String scheduleid;
    private String date;

    public EnrollPost(String scheduleid, String clinicid, String specialityid, String doctorid,
                      String date, String login, String password) {
        super(clinicid, specialityid, doctorid);
        this.login = login;
        this.password = password;
        this.date = date;
        this.scheduleid = scheduleid;
    }
}
